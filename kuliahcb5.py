def luas_lingkaran(r):
    luas = 3.14 * (r ** 2)
    print("Lingkaran dengan jari-jari", r, "memiliki luas", luas)

def keliling_lingkaran(r):
    keliling = 2 * 3.14 * r
    print("Lingkaran dengan jari-jari", r, "memiliki keliling", keliling)


