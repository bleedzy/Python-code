def luas_persegi(s):
    return s * s

def keliling_persegi(s):
    return 4 * s

def luas_persegi_panjang(p, l):
    return p * l

def keliling_persegi_panjang(p, l):
    return 2 * (p + l)

def luas_segitiga(a, t):
    return 0.5 * a * t

def keliling_segitiga(a, b, c):
    return a + b + c

def luas_lingkaran(r):
    return 3.14 * r * r

def keliling_lingkaran(r):
    return 2 * 3.14 * r