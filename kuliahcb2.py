#tipe data Boolean
print(True)

#tipe data String
print("Hello Word")
print('Selamat Datang')

#tipe data Integer
print(20)

#tipe data Float
print(3.14)

#tipe data Hexadecimal
print(0x1d3)

#tipe data Complex
print(7j)

#tipe data List
print([1,2,3,4,5])
print(["satu", "dua", "tiga"])

#tipe data Tuple
print((1,2,3,4,5))
print(("satu", "dua", "tiga"))

#tipe data Dictionary
print({"nama":"Bayu", 'umur':25})

#tipe data Dictionary dimasukan ke dalam variabel biodata
biodata = {"nama":"Adi", 'umur':14} #proses inisialisasi variabel biodata
print(biodata) #proses pencetakan variabel biodata yang berisi tipe data Dictionary
print(type(biodata)) #fungsi untuk mengecek jenis tipe data. akan tampil <class 'dict'> yang berarti dict adalah tipe data dictionar`